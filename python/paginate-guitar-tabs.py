def paginate_text(text, lines_per_page):
    lines = text.split('\n')
    paginated_text = []
    
    for i in range(0, len(lines), lines_per_page):
        page = '\n'.join(lines[i:i+lines_per_page])
        paginated_text.append(page)
    
    return paginated_text

def display_paginated_text(paginated_text):
    for page_num, page in enumerate(paginated_text, 1):
        print(f"Page {page_num}\n")
        print(page)
        print("\n" + "="*40 + "\n")  # Add a separator between pages

